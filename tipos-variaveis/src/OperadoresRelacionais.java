

public class OperadoresRelacionais {

    //Operadores relacionais avalaiam a relação entre as variáveis

    // == Quando desejamos verircar se uma variável é igual a outra.
    // != Quando desejamos verificar se uma variáveç é diferente da outra
    // > Quando desejamos verificar se uma variável é MAIOR que a outra
    // < Quando desejamos verificar se uma variável é MENOR que a outra
    // >= Quando desejamos verificar se uma variável é MAIOR que a outra
    // >= Quando desejamos verificar se uma variável é MAIOR ou IGUAL que a outra
    // <= Quando desejamos verificar se uma variável é MENOR ou IGUAL que a outra
    
    public static void main(String[] args) {
        int n1,n2;
        n1=1;
        n2=2;

        boolean simNao = n1 == n2;
        System.out.println(simNao);

        simNao = n1 < n2;
        System.out.println(simNao);

        simNao = n1 != n2;
        System.out.println(simNao);

        simNao = n1 <= n2;
        System.out.println(simNao);

       



    }

    
    


}
